public class Triangle
{
   
    int sideA, sideB, sideC;
    
    public Triangle (int sa, int sb, int sc){
        
        sideA = sa;
        sideB = sb;
        sideC = sc;
        
    }
    
    public int getSideA(){
        
        return sideA;
        
    }
    
    public int getSideB(){
    
        return sideB;
    
    }
    
    public int getSideC(){
    
        return sideC;
    
    }
    
}
